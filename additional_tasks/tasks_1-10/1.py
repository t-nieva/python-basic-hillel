# Написати програму, яка просить ввести 4 числа – кількість днів, годин,
# хвилин і секунд. Програма виводить цей час у секундах.

days = int(input("Введіть кількість днів: "))
hours = int(input("Введіть кількість годин: "))
minutes = int(input("Введіть кількість хвилин: "))
seconds = int(input("Введіть кількість секунд: "))

total_seconds = days * 24 * 3600 + hours * 3600 + minutes * 60 + seconds

print(f"Загальний час у секундах: {total_seconds}")
